Map::Map()
{
	wall = TextureManager::LoadTexture("assets/Wall.png");
	floor = TextureManager::LoadTexture("assets/Floor.png");

	loadMap(lvl1);

	src.x = 0;
	src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;

	dest.x = 0;
	dest.y = 0;
}

Map::~Map()
{
	SDL_DestroyTexture(wall);
	SDL_DestroyTexture(floor);
}

void Map::loadMap(int arr[23][40])
{
	for (int row = 0; row < 23; row++)
	{
		for (int column = 0; column < 40; column++)
		{
			map[row][column] = arr[row][column];
		}
	}
}

void Map::drawMap()
{
	int type = 0;

	for (int row = 0; row < 23; row++)
	{
		for (int column = 0; column < 40; column++)
		{
			type = map[row][column];

			dest.x = column * 32;
			dest.y = row * 32;

			switch (type)
			{
			case 0:
				TextureManager::Draw(wall, src, dest);
				break;
			case 1:
				TextureManager::Draw(floor, src, dest);
				break;
			default:
				break;
			}
		}
	}
}